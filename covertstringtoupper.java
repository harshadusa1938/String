package string;

public class covertstringtoupper 
{
//Java Tutorials for Beginners - Java Program to Convert a String to Upper and Lower Case
	public static void main(String[] args) 
	{
		String s1="hello java developer";
		String s2="HELLO JAVA DEVELOPER";
		
		System.out.println("Before Converting the String: " + s1);
		s1=s1.toUpperCase();
		System.out.println("Before Converting the String: " + s1);
		
		System.out.println("Before Converting the String: " + s2);
		s2=s2.toLowerCase();
		System.out.println("Before Converting the String: " + s2);
		

	}

}
